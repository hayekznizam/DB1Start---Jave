package br.com.db1.start.aula10;

public class Nome {
	//transformar para letra maiuscula
	public String transformarParaLetraMaisc(String nome) {
		return nome.toUpperCase();
		
	}
	
	//conta quantidade de caracteres 
	public int tamanhoDoNome(String nome) {
		return nome.length();
		
	}

}
